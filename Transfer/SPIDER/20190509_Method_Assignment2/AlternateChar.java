//Printing AlternateChar of the string

 
import java.util.Scanner;
class AlternateChar
{
	public static void main(String [] args)
	{

	System.out.println("Main Method statrs\n");
	Scanner sc= new Scanner(System.in);

	System.out.println("Please enter a String");
	String str= sc.nextLine();

	printAlternateChar(str);
	System.out.println("\nMain Method ends");

	}
	
	
	public static void printAlternateChar(String str)
	{             
	System.out.print("\nChar by Char data of " + str + " is:\n");
	int len = str.length();
	for(int i= 0;i< len;i+=2)
		{
		System.out.print(str.charAt(i)+ " ");             
		}
	}

}

 

 

