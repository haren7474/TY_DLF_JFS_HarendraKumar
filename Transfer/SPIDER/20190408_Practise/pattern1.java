class pattern1 
{
	public static void main(String[] args) 
	{
		 int i, j;
		 int n=6;
  
       
        for(i=0; i<n; i++) 
        { 
            for(j=5; j>=i; j--) 
            { 
                
                System.out.print("* "); 
            } 
  
            System.out.println(); 
		}
		
	}
}
