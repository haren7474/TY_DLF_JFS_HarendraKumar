package com.tyss.junit.MyFirstMaven;

public class Calculator 
{
	public int sum(int a, int b)
	{
		return a+b;
	}
	
	public int diff(int a, int b)
	{
		return a-b;
	}
	
	public int mult(int a, int b)
	{
		return a*b;
	}
	
	public double div(int a, int b)
	{
		return a/b;
	}
	
	public int findLength(String s)
	{
		return s.length();
	}
}
